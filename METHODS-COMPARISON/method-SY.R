  G1 <- function(m, ssq){
    is <- rep(seq(0,4), each=5)
    js <- rep(seq(0,4), 5)
    return( 10^sum((G1.A * sqrt(ssq) ^ (is/2) * abs(m) ^ (js/2))) )
