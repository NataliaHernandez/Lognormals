    is <- rep(seq(0,4), each=5)
    js <- rep(seq(0,4), 5)
    return( 10^sum((G3.A * sqrt(ssq) ^ (is/2) * abs(m) ^ (js/2))) )
  }

  if ( u2 > u1 ){
    tmp = u1
    u1 = u2
    u2 = tmp

    tmp = s1
    s1 = s2
    s2 = tmp
  }

  ssq1 <- s1^2
  ssq2 <- s2^2
  
  mw <- u2 - u1
  ssqw <- ssq1 + ssq2
  
  VZ = ssq1 - G1(mw, ssqw) ^ 2 - 2 * ssq1 / ssqw * G3(mw, ssqw) + G2(mw, ssqw)
  EZ = u1 + G1(mw, ssqw)

  return(c(EZ, sqrt(VZ)))
}
